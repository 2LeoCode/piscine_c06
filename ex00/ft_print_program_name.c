#include <unistd.h>

void	ft_putstr(char *str);

int		main(int argc, char **argv)
{
	ft_putstr(argv[0]);
	return (0);
}

void	ft_putstr(char *str)
{
	int i;

	i = -1;
	while (str[++i])
		write(1, &str[i], 1);
	write(1, "\n", 1);
}
